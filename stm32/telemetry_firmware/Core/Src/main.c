static float adc_to_volts(uint32_t raw)
{
    return ((float)raw * VREF) / ADC_MAX;
}

static float read_voltage_v(void)
{
    uint32_t raw = read_adc_channel(ADC_CHANNEL_0);
    float adc_v = adc_to_volts(raw);
    return adc_v * VOLTAGE_GAIN;
}

static float read_current_a(void)
{
    uint32_t raw = read_adc_channel(ADC_CHANNEL_1);
    float adc_v = adc_to_volts(raw);
    return (adc_v - CURRENT_OFFSET_V) / CURRENT_SENSITIVITY_VPA;
}

static void set_pwm_percent(float pct)
{
    if (pct < 0.0f)
    {
        pct = 0.0f;
    }
    if (pct > 100.0f)
    {
        pct = 100.0f;
    }

    uint32_t arr = __HAL_TIM_GET_AUTORELOAD(&htim3);
    uint32_t compare = (uint32_t)((pct / 100.0f) * (float)(arr + 1U));

    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, compare);
}

static void uart_send_line(const char *s)
{
    if (HAL_UART_Transmit(&huart1, (uint8_t *)s, strlen(s), HAL_MAX_DELAY) != HAL_OK)
    {
        Error_Handler();
    }
}
